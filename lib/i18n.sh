# Translation helper.
# Usage: _t "key" [arg1 arg2 ...]
# If extra args are provided, uses printf-style formatting.
_t() {
    local key="$1"
    local template="${I18N[$key]:-$key}"
    if [[ $# -gt 1 ]]; then
        shift
        # Use printf safely: the template is expected to contain format specifiers
        printf "$template" "$@"
    else
        printf "%s" "$template"
    fi
}

# Print translation followed by newline
_tn() {
    _t "$@"
    printf "\n"
}

